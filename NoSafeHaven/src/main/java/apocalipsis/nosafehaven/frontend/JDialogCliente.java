    /**
     * Maneja el evento de acci√≥n del bot√≥n "Listo" para establecer la conexi√≥n.
     *
     * Este m√©todo se ejecuta cuando el usuario hace clic en el bot√≥n "Listo" en
     * la interfaz. Valida la direcci√≥n y el puerto ingresados por el usuario, y
     * si son v√°lidos, los asigna a las variables correspondientes y cierra el
     * di√°logo.
     *
     * Validaciones realizadas: - La direcci√≥n no debe estar vac√≠a. - El puerto
     * debe estar en el rango de 1024 a 65535.
     *
     * En caso de que el puerto no sea un n√∫mero v√°lido, se muestra un mensaje
     * de error. Si ocurre cualquier otro error, se muestra un mensaje con la
     * descripci√≥n del problema.
     *
     * @param evt El evento de acci√≥n generado al hacer clic en el bot√≥n.
     */
    private void botonListoConexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListoConexionActionPerformed

        try {
            String dir = direccionTextField.getText();
            int port = Integer.parseInt(puertoTextField.getText());

            if (dir.isEmpty()) {
                JOptionPane.showMessageDialog(this, "La direcci√≥n no puede estar vac√≠a");
                return;
            }

            if (port < 1024 || port > 65535) {
                JOptionPane.showMessageDialog(this, "Puerto fuera de rango (1024‚Äì65535)");
                return;
            }

            direccion = dir;
            puerto = port;
            confirmado = true;

            dispose();  // Cierra el JDialog

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Puerto inv√°lido");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al iniciar el servidor: " + ex.getMessage());
        }
    }//GEN-LAST:event_botonListoConexionActionPerformed

